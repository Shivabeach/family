    <div class="dnablock">
      <h3>60,000 years old</h3>
        <p class="mid-size">Branch: P143</p>
        <p>Age: About 60,000 years old</p>
        <p>Location of Origin: Southwest Asia</p>
    </div>
        <p>This mutation is one of the oldest thought to have occurred outside of Africa and therefore marks a pivotal moment in the evolution of modern humans. Moving along the coastline, members of this lineage were some of the earliest settlers in Asia, Southeast Asia, and Australia.</p>

        <p>But why would man have first ventured out of the familiar African hunting grounds and into unexplored lands? The first migrants likely ventured across the Bab-al Mandeb strait, a narrow body of water at the southern end of the Red Sea, crossing into the Arabian Peninsula and soon after developing mutation P143—perhaps 60,000 years ago. These beachcombers would make their way rapidly to India and Southeast Asia, following the coastline in a gradual march eastward. By 50,000 years ago, they had reached Australia. These were the ancestors of some of today’s Australian Aborigines.</p>

        <p>It is also likely that a fluctuation in climate may have contributed to your ancestors’ exodus out of Africa. The African ice age was characterized by drought rather than by cold. Around 50,000 years ago, though, the ice sheets of the Northern Hemisphere began to melt, introducing a short period of warmer temperatures and moister climate in Africa and the Middle East. Parts of the inhospitable Sahara briefly became habitable. As the drought-ridden desert changed to a savanna, the animals hunted by your ancestors expanded their range and began moving through the newly emerging green corridor of grasslands.</p>
