    <div class="dnablock">
      <h3>10,000 Years Ago</h3>

        <p class="mid-size">Branch: M253</p>
        <p>Age: 5,500 – 26,000 Years Ago</p>
        <p>Location of Origin: Europe</p>
    </div>
        <p>When ice covered much of Europe, the cold and lack of food sources forced groups containing men from this lineage into refugia. It was from these refugia on the Iberian Peninsula, to the north of the Black Sea, and elsewhere, that members of this lineage emerged around 10,000 years ago.</p>

        <p>Emerging from the refugia<sub>2</sub>, groups expanded across Europe and back toward West Asia in successive waves. The highest frequencies of this lineage are in Scandinavian countries. This may be due to early founders during a time of extremely small settling population groups.</p>

        <p>Today, this lineage is present throughout Europe. It is about 40 percent of the population of Norway. It is present in Finland at around 35 percent of male lineages. In the British Isles, it is between 10 and 22 percent of male lineages. It is between 10 and 11 percent of French and about 18 percent of German male lineages. It is about 4 percent of the male population of Spain, between 2 and 3 percent of the male population of Italy, and about 2 percent of the male population of Greece.</p>

        <p>In West Asia, it is present in trace frequencies of less than 1 percent. However, it is about 2 percent of male lineage in Lebanon and about 4 percent of male lineages in Jordan.
        Notable People</p>

        <p>Swedish statesman Birger Magnusson and Russian writer Leo Tolstoy were both from this lineage.<sub>1</sub></p>
