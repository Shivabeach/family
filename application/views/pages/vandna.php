    <article id="70">
    <div class="dnablock">
      <h3>70,000 Years ago</h3>
            <p class="mid-size">Branch: M168</p>
            <p>Age: About 70,000 years ago</p>
            <p>Location of Origin: East Africa</p>
    </div>
            <p>When humans left Africa, they migrated across the globe in a web of paths that spread out like the branches of a tree, each limb of migration identifiable by a marker in our DNA. For male lineages, the M168 branch was one of the first to leave the African homeland.</p>

            <p>The man who gave rise to the first genetic marker in your lineage probably lived in northeast Africa in the region of the Rift Valley, perhaps in present-day Ethiopia, Kenya, or Tanzania. Scientists put the most likely date for when he lived at around 70,000 years ago. His descendants became the only lineage to survive outside of Africa, making him the common ancestor of every non-African man living today.</p>

            <p>Your nomadic ancestors would have followed the good weather and the animals they hunted, although the exact route they followed remains to be determined. In addition to a favorable change in climate, around this same time there was a great leap forward in modern humans’ intellectual capacity. Many scientists believe that the emergence of language gave us a huge advantage over other early humanlike species. Improved tools and weapons, the ability to plan ahead and cooperate with one another, and an increased capacity to exploit resources in ways we hadn’t been able to earlier allowed modern humans to rapidly migrate to new territories, exploit new resources, and replace other hominids such as the Neanderthals.</p>
            <a class="m20" href="#top">Top</a>
    </article>

