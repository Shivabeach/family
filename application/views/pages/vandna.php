    <div class="dnablock">
      <h3>55,000 Years ago</h3>

        <p class="mid-size">Branch: M89 </p>
        <p>Age: About 55,000 Years Old</p>
        <p>Location of Origin: Southwest Asia</p>
   </div>
        <p>The next male ancestor in your ancestral lineage is the man who gave rise to M89, a marker found in 90 to 95 percent of all non-Africans. This man was likely born around 55,000 years ago in Middle East.</p>

        <p>While many of the descendants of M89 remained in the Middle East, others continued to follow the great herds of wild game through what is now modern-day Iran, then north to the Caucasus and the steppes of Central Asia. These semiarid, grass-covered plains would eventually form an ancient “superhighway” stretching from France to Korea. A smaller group continued moving north from the Middle East to Anatolia and the Balkans, trading familiar grasslands for forests and high country.</p>
