          <p>The common direct paternal ancestor of all men alive today was born in Africa between 300,000 and 150,000 years ago. Dubbed “Y-chromosome Adam” by the popular press, he was neither the first human male nor the only man alive in his time. He was, though, the only male whose Y-chromosome lineage is still around today. All men, including your direct paternal ancestors, trace their ancestry to one of this man’s descendants. The oldest Y-chromosome lineages in existence, belonging to the A00 branch of the tree, are found only in African populations.</p>

          <p>Around 100,000 years ago the mutation named P305 occurred in the Y chromosome of a man in Africa. This is one of the oldest known mutations that is not shared by all men. Therefore, it marks one of the early splits in the human Y-chromosome tree, which itself marks one of the earliest branching points in modern human evolution. The man who first carried this mutation lived in Africa and is the ancestor to more than 99.9% of paternal lineages today. In fact, men who do not carry this mutation are so rare that its importance in human history was discovered only in the past two years.</p>

          <p>As P305-bearing populations migrated around the globe, they picked up additional markers on their Y chromosomes. Today, there are no known P305-bearing individuals without these additional markers.</p>
