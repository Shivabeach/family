        <h3>
          About 55,000 Years Ago
        </h3>   
        <p class="mid-size">Branch: N</p>
          <p>Age: About 55,000 Years Ago</p>
          <p>Location of Origin: West Asia</p> 
    </div>

          <p>After several thousand years in the Near East, individuals belonging to a new group called haplogroup R began to move out and explore the surrounding areas. Some moved south, migrating back into northern Africa. Others went west across Anatolia (present-day Turkey) and north across the Caucasus Mountains of Georgia and southern Russia. Still others headed east into the Middle East, and on to Central Asia. All of these individuals had one thing in common: they shared a female ancestor from the N clan, a recent descendant of the migration out of Africa.</p>

          <p>The story of haplogroup R is complicated, however, because these individuals can be found almost everywhere, and because their origin is quite ancient. In fact, the ancestor of haplogroup R lived relatively soon after humans moved out of Africa during the second wave, and her descendants undertook many of the same migrations as her own group, N.</p>

          <p>Because the two groups lived side by side for thousands of years, it is likely that the migrations radiating out from the Near East comprised individuals from both of these groups. They simply moved together, bringing their N and R lineages to the same places around the same times. The tapestry of genetic lines became quickly entangled, and geneticists are currently working to unravel the different stories of haplogroups N and R, since they are found in many of the same far-reaching places.</p>
          <h4>Point of Interest</h4>

          <p>Descendants of this line dominate the European maternal landscape, making up 75 to 95 percent of the lineages there.</p>
