    </article>

    <article id="50">
    <div class="dnablock">
      <h3>50,000 Yeara Ago</h3>
        <p class="mid-size">Branch: M578</p>
        <p>Age: About 50,000 Years Old</p>
        <p>Location of Origin: Southwest Asia</p>
    </div>

        <p>After settling in Southwest Asia for several millennia, humans began to expand in various directions, including east and south around the Indian Ocean, but also north toward Anatolia and the Black and Caspian Seas. The first man to acquire mutation M578 was among those that stayed in Southwest Asia before moving on.</p>

        <p>Fast-forwarding to about 40,000 years ago, the climate shifted once again and became colder and more arid. Drought hit Africa and the Middle East and the grasslands reverted to desert, and for the next 20,000 years, the Saharan Gateway was effectively closed. With the desert impassable, your ancestors had two options: remain in the Middle East, or move on. Retreat back to the home continent was not an option.</p>
