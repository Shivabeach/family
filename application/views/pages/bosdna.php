          <p>Haplogroup J has a very wide distribution, and is present as far east as the Indus Valley bordering India and Pakistan, and as far south as the Arabian Peninsula. It is also common in eastern and northern Europe. Although your haplogroup was present during the early and middle Upper Paleolithic, J is largely considered one of the main genetic signatures of the Neolithic expansions.</p>

          <p>While groups of hunter-gatherers and subsistence fishermen had been occupying much of Eurasia for tens of thousands of years, around ten thousand years ago a group of modern humans living in the Fertile Crescent—present-day eastern Turkey and northern Syria—began domesticating the plants, nuts, and seeds they had been collecting. What resulted were the world’s first agriculturalists, and this new cultural era is typically referred to as the Neolithic.</p>

          <p>Groups of individuals able to support larger populations with this reliable food source began migrating out of the Middle East, bringing their new technology with them. By then, humans had already settled much of the surrounding areas, but this new agricultural technology proved too successful to ignore, and the surrounding groups quickly copied these new immigrants. Interestingly, DNA data indicate that while these new agriculturalists were incredibly successful at planting their technology among the surrounding groups, they were far less successful at planting their own genetic seed. Agriculture was quickly and widely adopted, but the lineages carried by these Neolithic expansions are found today at low frequencies.</p>

          <p>Your haplogroup has greater diversity in the Near East than in Europe, indicating a homeland for J’s most recent common ancestor around the Levant, a coastal region in what is now Lebanon. It reaches its highest frequency in Arabia, comprising around 25% of the Bedouin and Yemeni. But genetic evidence indicates that these populations have either experienced low population sizes or undergone a founder event, indicating that the higher frequency is more reflective of these bottleneck events rather than this region actually constituting the geographic origin of haplogroup J.<sub>1</sub></p>
      
          <h4>Point of Interest</h4>

          <p>This line reaches its highest frequency in Arabia, comprising 25 percent of Bedouin and Yemeni lineages.</p>
          <h4>Notable People</h4>

          <p>Francesco Petrarca, the father of Humanism, and Richard III, King of England, were members of this lineage.</p>
