        <h3>
        60,000 years ago
      </h3>
          <p class="mid-size">Branch: N</p>
          <p>Age: About 60,000 Years Ago</p>
          <p>Location of Origin: East Africa Or Asia</p> 
    </div>           

          <p>Your next ancestor is the woman whose descendants formed haplogroup N. Haplogroup N comprises one of two groups that were created by the descendants of L3.</p>

          <p>One of these two groups of individuals moved north rather than east and left the African continent across the Sinai Peninsula, in present-day Egypt. Also faced with the harsh desert conditions of the Sahara, these people likely followed the Nile basin, which would have proved a reliable water and food supply in spite of the surrounding desert and its frequent sandstorms.</p>

          <p>Descendants of these migrants eventually formed haplogroup N. Early members of this group lived in the eastern Mediterranean region and western Asia, where they likely coexisted for a time with other hominids such as Neanderthals. Excavations in Israel’s Kebara Cave (Mount Carmel) have unearthed Neanderthal skeletons as recent as 60,000 years old, indicating that there was both geographic and temporal overlap of these two hominids.  This likely accounts for the presence of Neanderthal DNA in people living outside of Africa.</p>

          <p>Some members bearing mutations specific to haplogroup N formed many groups of their own which went on to populate much of the rest of the globe. These descendants are found throughout Asia, Europe, India, and the Americas. However, because almost all of the mitochondrial lineages found in the Near East and Europe descend from N, it is considered a western Eurasian haplogroup.</p>

          <p>After several thousand years in the Near East, members of your group began moving into unexplored nearby territories, following large herds of migrating game across vast plains. These groups broke into several directions and made their way into territories surrounding the Near East.</p>

          <p>Today, haplogroup N individuals who headed west are prevalent in Turkey and the eastern Mediterranean, they are found further east in parts of Central Asia and the Indus Valley of Pakistan and India. And members of your haplogroup who headed north out of the Levant across the Caucasus Mountains have remained in southeastern Europe and the Balkans. Importantly, descendants of these people eventually went on to populate the rest of Europe, and today comprise the most frequent mitochondrial lineages found there.</p>           
          <h4>Point of Interest</h4>
          <p>This line and its sister lineage are the only two founding lineages to expand out of Africa.</p>

