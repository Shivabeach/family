          <p>This woman’s descendants would eventually account for both out-of-Africa maternal lineages, significant population migrations in Africa, and even take part in the Atlantic Slave Trade related dispersals from Africa.</p>

          <p>The common direct maternal ancestor to all women alive today was born in East Africa around 180,000 years ago. Dubbed “Mitochondrial Eve” by the popular press, she represents the root of the human family tree. Eve gave rise to two descendant lineages known as L0 and L1’2’3’4’5’6, characterized by a different set of genetic mutations their members carry.</p>

          <p>Current genetic data indicates that indigenous people belonging to these groups are found exclusively in Africa. This means that, because all humans have a common female ancestor, and because the genetic data shows that Africans are the oldest groups on the planet, we know our species originated there.</p>

          <p>Eventually, L1’2’3’4’5’6 gave rise to L3 in East Africa. It is a similar story: an individual underwent a mutation to her mitochondrial DNA, which was passed onto her children. The children were successful, and their descendants ultimately broke away from L1’2’3’4’5’6, eventually separating into a new group called L3.</p>

          <p>While L3 individuals are found all over Africa, L3 is important for its movements north. Your L3 ancestors were significant because they are the first modern humans to have left Africa, representing the deepest branches of the tree found outside of that continent.</p>

          <p>From there, members of this group went in a few different directions. Many stayed on in Africa, dispersing to the west and south.  Some L3 lineages are predominant in many Bantu-speaking groups who originated in west-central Africa, later dispersing throughout the continent and spreading this L3 lineage from Mali to South Africa.  Today, L3 is also found in many African-Americans.</p>

          <p>Other L3 individuals, your ancestors, kept moving northward, eventually leaving the African continent completely. These people gave rise to two important macro-haplogroups (M and N) that went on to populate the rest of the world.</p>

          <p>Why would humans have first ventured out of the familiar African hunting grounds and into unexplored lands? It is likely that a fluctuation in climate may have provided the impetus for your ancestors’ exodus out of Africa.</p>

          <p>The African Ice Age was characterized by drought rather than by cold. Around 50,000 years ago the ice sheets of northern Europe began to melt, introducing a period of warmer temperatures and moister climate in Africa. Parts of the inhospitable Sahara briefly became habitable. As the drought-ridden desert changed to savanna, the animals your ancestors hunted expanded their range and began moving through the newly emerging green corridor of grasslands. Your nomadic ancestors followed the good weather and plentiful game northward across this Saharan Gateway, although the exact route they followed remains to be determined.</p>
          <h4>Point of Interest</h4>

          <p>The L branch is shared by all women alive today, both in Africa and around the world. The L3 branch is the major maternal branch from which all mitochondrial DNA lineages outside of Africa arose.</p>          
