      <h3>Denmark</h3>
      <p>
        In the year 1640 or there abouts, in Jutland Denmark, a man named Matthys Corneliussen was born. Nothing is known about his parents or his life in general there. He immigrated to the US, arriving at New Netherlands in 1663.  This is where the VanHorn family begins. Had he never immigrated, we would not be here today. A great example of for every action there is a result.
      </p>

      <p>
        He married Fytje Sophia Brouwer(1664-1709) on February 20, 1692. They had two children during their marriage. He died in 1703 in Monmouth County, New Jersey, at the age of 63. It should be noted the date of his dying is not known as a fact.
      </p>
      <p>
        The VanHorns are not like the Bosticks, in that there are no 2 long standing familys that lead to our Dad. The Bosticks have the Reeds, Sholes(Mom's Grandmother), Stoddards and the Bostick families, while the VanHorns have, the VanHorns. Of course there are still some longer lineage in Dad's family. The Clays(His grandmother), the Hillyers and the Roricks. But this family doesnt really open up to a huge array of names like Mom's family does. And of course, Mom's family goes back much further then Dad's does.
      </p>
    </article>

