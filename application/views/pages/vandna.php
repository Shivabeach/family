      <h3>
        80,000 years ago
      </h3>
          <p class="mid-size">Branch: M42</p>
          <p>Age: About 80,000 Years Ago</p>
          <p>Location of Origin: East Africa</p>
    </div>

          <p>Around 80,000 years ago, the BT branch of the Y-chromosome tree was born, defined by many genetic markers, including M42. The common ancestor of most men living today, some of this man’s descendants would begin the journey out of Africa to the Middle East and India. Some small groups from this line would eventually reach the Americas, while other groups would settle in Europe, and some would remain near their ancestral homeland in Africa.</p>

          <p>Individuals from this line whose ancestors stayed in Africa often practice cultural traditions that resemble those of the distant past. For example, they often live in traditional hunter-gatherer societies. These include the Mbuti and Biaka Pygmies of central Africa, as well as Tanzania’s Hadza.</p>

          <p>The M42 branch is shared by almost all men alive today, both in Africa and around the world.</p>
